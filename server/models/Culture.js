import mongoose from "mongoose";

const cultureSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Culture = mongoose.model("Culture", cultureSchema);
export default Culture;

